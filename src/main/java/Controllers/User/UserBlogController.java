    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        String path = request.getRequestURI();
        NumberCustom number = new NumberCustom();
        BlogDao blogDao = new BlogDao();
        if(path.endsWith("/CWU/blog")) {
            List<Blog> blogs = blogDao.getAllActive();
            request.setAttribute("blogs", blogs);
            request.getRequestDispatcher("/User/view/blog.jsp").forward(request, response);
        } else if(path.startsWith("/CWU/blog/detail/")) {
            String paths[] = path.split("/");
            int id = number.getInt(paths[paths.length -1]);
            Blog blog = blogDao.getByIdActive(id);
            if(blog == null) {
                response.sendRedirect("/CWU/404");
            } else {
                request.setAttribute("blog", blog);
                request.getRequestDispatcher("/User/view/blogDetail.jsp").forward(request, response);
            }
        }
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        processRequest(request, response);
    }

