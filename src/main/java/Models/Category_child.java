package Models;

/**
 *
 * @author LENOVO
 */
public class Category_child {
    private int id;
    private String name;
    private int status;
    private int category_id;

    public Category_child() {
    }

    public Category_child(int id, String name, int status, int category_id) {
        this.id = id;
        this.name = name;
        this.status = status;
        this.category_id = category_id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getCategory_id() {
        return category_id;
    }

    public void setCategory_id(int category_id) {
        this.category_id = category_id;
    }
    
}
